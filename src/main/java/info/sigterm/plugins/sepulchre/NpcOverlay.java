package info.sigterm.plugins.sepulchre;

import java.awt.BasicStroke;
import java.awt.Color;
import java.awt.Dimension;
import java.awt.Graphics2D;
import java.awt.Polygon;
import java.awt.Shape;
import java.util.Set;
import javax.inject.Inject;
import net.runelite.api.Client;
import net.runelite.api.NPC;
import net.runelite.api.NPCComposition;
import net.runelite.api.Perspective;
import net.runelite.api.coords.LocalPoint;
import net.runelite.client.ui.overlay.Overlay;
import net.runelite.client.ui.overlay.OverlayLayer;
import net.runelite.client.ui.overlay.OverlayPosition;

class NpcOverlay extends Overlay
{
	private final Client client;
	private final SepulchrePlugin plugin;
	private final SepulchreConfig config;

	@Inject
	private NpcOverlay(Client client, SepulchrePlugin plugin, SepulchreConfig config)
	{
		super(plugin);
		setPosition(OverlayPosition.DYNAMIC);
		setLayer(OverlayLayer.ABOVE_SCENE);
		this.client = client;
		this.plugin = plugin;
		this.config = config;
	}

	@Override
	public Dimension render(Graphics2D graphics)
	{
		Set<NPC> npcs = plugin.getNpcs();
		if (npcs.isEmpty())
		{
			return null;
		}

		Color color = config.highlightColor();
		for (NPC npc : plugin.getNpcs())
		{
			NPCComposition npcComposition = npc.getComposition();
			int size = npcComposition.getSize();
			LocalPoint lp = npc.getLocalLocation();
			Polygon tilePoly = Perspective.getCanvasTileAreaPoly(client, lp, size);

			renderPoly(graphics, color, tilePoly);
		}

		return null;
	}

	private void renderPoly(Graphics2D graphics, Color color, Shape polygon)
	{
		if (polygon != null)
		{
			graphics.setColor(color);
			graphics.setStroke(new BasicStroke(2));
			graphics.draw(polygon);
			graphics.setColor(new Color(color.getRed(), color.getGreen(), color.getBlue(), 20));
			graphics.fill(polygon);
		}
	}
}
